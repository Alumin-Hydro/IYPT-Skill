function g = dphi_dz(R, L, Ms, r, z, mu0)
% DPHI_DZ  有限长均匀磁化圆柱在半径 r 圆环上的磁通梯度。**闭式。**
%
% 安培模型：均匀磁化圆柱 == 侧面束缚面电流 K = M_s。于是
%
%     Phi(r,z) = M_s * int_{z-L/2}^{z+L/2} M(R,r,u) du
%
% **两个积分限都含 z，被积函数不含 z** —— Leibniz 求导，积分整个消失：
%
%     dPhi/dz = M_s [ M(R,r,z+L/2) - M(R,r,z-L/2) ]
%
% 原本的三重数值积分塌成两次椭圆积分求值。**能解析给出的导数，永远不要数值求。**
%
% 物理：这就是「磁体两个端面的等效电流环之差」——它天然是 z 的奇函数，所以涡流分布
% 必然是**反对称双峰**（前方排斥、后方吸引，两者都阻碍下落）。这正是 Lenz 定律。
% 数值上也证实了：峰位落在 |z| = 5.03 mm ~ L/2 = 5.00 mm，即磁体端面上，
% 而不是点偶极子预言的 a/2 = 3.00 mm。
%
% 本文件由 iypt-simulation 从已验证的 Python 实现移植；生成时未在 MATLAB 中执行。

    g = Ms .* (mutual(R, r, z + L/2, mu0) - mutual(R, r, z - L/2, mu0));
end
